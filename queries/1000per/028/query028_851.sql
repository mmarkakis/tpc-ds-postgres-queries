      where ss_quantity between 11 and 15
        and (ss_list_price between 167 and 167+10
          or ss_coupon_amt between 10150 and 10150+1000
          or ss_wholesale_cost between 51 and 51+20)) B3,
     (select avg(ss_list_price) B4_LP
