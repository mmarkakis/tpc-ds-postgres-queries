      where ss_quantity between 11 and 15
        and (ss_list_price between 132 and 132+10
          or ss_coupon_amt between 13756 and 13756+1000
          or ss_wholesale_cost between 54 and 54+20)) B3,
     (select avg(ss_list_price) B4_LP
