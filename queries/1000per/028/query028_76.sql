      where ss_quantity between 11 and 15
        and (ss_list_price between 184 and 184+10
          or ss_coupon_amt between 16485 and 16485+1000
          or ss_wholesale_cost between 33 and 33+20)) B3,
     (select avg(ss_list_price) B4_LP
